#!/usr/bin/env python3

from __future__ import annotations

import json
from datetime import datetime
from pathlib import Path


ROLE_ASSIGNMENTS = [
    ("NEO4J_HTTP", "neo4j_https"),
    ("NEO4J_BOLT", "neo4j_bolt"),
    ("POSTGRES", "postgres_tcp"),
    ("CONSUMER_HTTP", "reserved_consumer_http"),
]


def normalize_public_host(raw_value: str) -> str:
    return raw_value.strip().removeprefix("https://").removeprefix("http://").rstrip("/")


def main() -> int:
    repo_root = Path(__file__).resolve().parents[1]
    tunnels_path = repo_root / "tunnels.json"
    runtime_dir = repo_root / ".runtime"
    env_path = runtime_dir / "tunnels.env"
    public_path = runtime_dir / "public_hosts.json"
    logs_raw_dir = repo_root / "_logs" / "raw"
    runlog_path = repo_root / "_logs" / "RUNLOG.md"

    if not tunnels_path.exists():
        raise SystemExit(f"missing required runtime file: {tunnels_path}")

    tunnels = json.loads(tunnels_path.read_text(encoding="utf-8"))
    if not isinstance(tunnels, list) or len(tunnels) != 4:
        raise SystemExit("tunnels.json must contain exactly 4 tunnel objects")

    runtime_dir.mkdir(parents=True, exist_ok=True)
    logs_raw_dir.mkdir(parents=True, exist_ok=True)
    runlog_path.touch(exist_ok=True)

    run_ts = datetime.now().strftime("%y%m%d_%H%M%S")
    env_lines = [
        "# Generated by scripts/prepare_runtime.py",
        f"RUN_TS={run_ts}",
        "",
        "# Shared demo credentials used by both the DinD stack and the host-side client.",
        "NEO4J_USER=neo4j",
        "NEO4J_PASSWORD=tunnel-demo-neo4j",
        "POSTGRES_USER=tunnel_demo",
        "POSTGRES_PASSWORD=tunnel-demo-postgres",
        "POSTGRES_DB=tunnel_demo",
        "",
        "# Fixed host-side listener ports used by the local Python TCP bridges.",
        "HOST_NEO4J_BOLT_FORWARD_PORT=17687",
        "HOST_POSTGRES_FORWARD_PORT=15432",
        "",
        "# Experiment timing defaults.",
        "EXPERIMENT_DURATION_SECONDS=30",
        "EXPERIMENT_CYCLE_INTERVAL_SECONDS=10",
    ]
    public_hosts: dict[str, dict[str, str]] = {}

    for (prefix, role_name), entry in zip(ROLE_ASSIGNMENTS, tunnels):
        if not isinstance(entry, dict):
            raise SystemExit(f"invalid tunnel entry for {prefix}: expected object")

        public_host = normalize_public_host(str(entry.get("url", "")))
        token = str(entry.get("token", "")).strip()
        tunnel_name = str(entry.get("tunnel_name", "")).strip()

        if not public_host or not token:
            raise SystemExit(f"tunnel entry for {prefix} must include non-empty url and token")

        public_hosts[prefix] = {
            "role": role_name,
            "tunnel_name": tunnel_name,
            "public_host": public_host,
        }

        env_lines.append(f"{prefix}_PUBLIC_HOST={public_host}")
        env_lines.append(f"{prefix}_TUNNEL_TOKEN={token}")

    env_path.write_text("\n".join(env_lines) + "\n", encoding="utf-8")
    public_path.write_text(json.dumps(public_hosts, indent=2) + "\n", encoding="utf-8")

    print(f"generated {env_path.relative_to(repo_root)}")
    print(f"generated {public_path.relative_to(repo_root)}")
    print(f"ensured {logs_raw_dir.relative_to(repo_root)} exists")
    print(f"ensured {runlog_path.relative_to(repo_root)} exists")
    print("tunnel assignments:")
    for prefix, metadata in public_hosts.items():
        print(f"- {prefix}: {metadata['public_host']} ({metadata['tunnel_name'] or 'unnamed'})")

    return 0
if __name__ == "__main__":
    raise SystemExit(main())
