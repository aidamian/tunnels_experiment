#!/usr/bin/env python3

from __future__ import annotations

import json
from datetime import datetime
from pathlib import Path


ROLE_ASSIGNMENTS = [
    ("NEO4J_HTTP", "neo4j_https"),
    ("NEO4J_BOLT", "neo4j_bolt"),
    ("POSTGRES", "postgres_tcp"),
    ("CONSUMER_HTTP", "consumer_http"),
]


def main() -> int:
    repo_root = Path(__file__).resolve().parents[1]
    tunnels_path = repo_root / "tunnels.json"
    runtime_dir = repo_root / ".runtime"
    env_path = runtime_dir / "tunnels.env"
    public_path = runtime_dir / "public_hosts.json"

    if not tunnels_path.exists():
        raise SystemExit(f"missing required runtime file: {tunnels_path}")

    tunnels = json.loads(tunnels_path.read_text())
    if not isinstance(tunnels, list) or len(tunnels) != 4:
        raise SystemExit("tunnels.json must contain exactly 4 tunnel objects")

    runtime_dir.mkdir(parents=True, exist_ok=True)
    run_ts = datetime.now().strftime("%y%m%d_%H%M%S")

    env_lines = [
        "# Generated by scripts/prepare_runtime.py",
        f"RUN_TS={run_ts}",
    ]
    public_hosts: dict[str, dict[str, str]] = {}

    for (prefix, role_name), entry in zip(ROLE_ASSIGNMENTS, tunnels):
        if not isinstance(entry, dict):
            raise SystemExit(f"invalid tunnel entry for {prefix}: expected object")

        url = str(entry.get("url", "")).strip()
        token = str(entry.get("token", "")).strip()
        tunnel_name = str(entry.get("tunnel_name", "")).strip()

        if not url or not token:
            raise SystemExit(f"tunnel entry for {prefix} must include non-empty url and token")

        public_host = url.removeprefix("https://").rstrip("/")
        public_hosts[prefix] = {
            "role": role_name,
            "tunnel_name": tunnel_name,
            "public_host": public_host,
        }
        env_lines.append(f"{prefix}_PUBLIC_HOST={public_host}")
        env_lines.append(f"{prefix}_TUNNEL_TOKEN={token}")

    env_path.write_text("\n".join(env_lines) + "\n")
    public_path.write_text(json.dumps(public_hosts, indent=2) + "\n")

    print(f"generated {env_path.relative_to(repo_root)}")
    print(f"generated {public_path.relative_to(repo_root)}")
    print("tunnel assignments:")
    for prefix, metadata in public_hosts.items():
        print(f"- {prefix}: {metadata['public_host']} ({metadata['tunnel_name'] or 'unnamed'})")

    return 0


if __name__ == "__main__":
    raise SystemExit(main())
