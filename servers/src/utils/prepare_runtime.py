"""Generate server-owned runtime files from the local tunnel inventory."""

from __future__ import annotations

import argparse
import json
import sys
from datetime import datetime
from pathlib import Path


SRC_DIR = Path(__file__).resolve().parents[1]
if str(SRC_DIR) not in sys.path:
  sys.path.insert(0, str(SRC_DIR))


from utils.demo_config import (
  NEO4J_PASSWORD,
  NEO4J_USER,
  POSTGRES_DB,
  POSTGRES_PASSWORD,
  POSTGRES_USER,
)
from utils.sdk_logging import build_console_logger, log_message


# The tunnel-role mapping is intentionally fixed by the project plan. The
# orchestration layer can discover services dynamically, but tunnel indexes
# 1/2/3/4 still have stable meanings that the rest of the repository depends on.
ROLE_ASSIGNMENTS = [
  ("NEO4J_HTTP", "neo4j_https"),
  ("NEO4J_BOLT", "neo4j_bolt"),
  ("POSTGRES", "postgres_tcp"),
  ("APP_UI", "app_ui_https"),
]
DEFAULT_SERVER_SERVICES = "neo4j,pgsql"


def parse_args() -> argparse.Namespace:
  """Parse CLI arguments for server runtime generation."""
  parser = argparse.ArgumentParser(description="Generate server runtime state from servers/tunnels.json.")
  parser.add_argument(
    "--enabled-services",
    default=DEFAULT_SERVER_SERVICES,
    help="Comma-separated server service keys to launch inside dind-host-server.",
  )
  parser.add_argument(
    "--run-ts",
    help="Optional run identifier override. Defaults to the current timestamp.",
  )
  return parser.parse_args()


def normalize_public_host(raw_value: str) -> str:
  """Normalize a public tunnel URL down to its hostname.

  Parameters
  ----------
  raw_value:
    Raw URL or hostname value read from `tunnels.json`.

  Returns
  -------
  str
    Hostname with any scheme prefix and trailing slash removed.

  Examples
  --------
  >>> normalize_public_host("https://example.com/")
  'example.com'
  """
  # Downstream tools decide whether they need `https://` or `wss://`, so the
  # generated runtime file stores the bare hostname only.
  return raw_value.strip().removeprefix("https://").removeprefix("http://").rstrip("/")


def main() -> int:
  """Build the server-side runtime file used by the DinD stack."""
  args = parse_args()
  log = build_console_logger("servers-runtime")
  server_root = Path(__file__).resolve().parents[2]
  repo_root = server_root.parent
  tunnels_path = server_root / "tunnels.json"
  runtime_dir = server_root / ".runtime"
  dind_env_path = runtime_dir / "dind.env"
  logs_raw_dir = server_root / "_logs" / "raw"

  if not tunnels_path.exists():
    raise SystemExit(f"missing required runtime file: {tunnels_path}")

  # The repository deliberately expects exactly four tunnel objects because the
  # fourth tunnel is now reserved for the app-host HTTPS UI.
  tunnels = json.loads(tunnels_path.read_text(encoding="utf-8"))
  if not isinstance(tunnels, list) or len(tunnels) != 4:
    raise SystemExit("tunnels.json must contain exactly 4 tunnel objects")

  runtime_dir.mkdir(parents=True, exist_ok=True)
  logs_raw_dir.mkdir(parents=True, exist_ok=True)

  # Generate a fresh run identifier once so every later artifact shares the
  # same timestamp-based naming convention.
  run_ts = args.run_ts or datetime.now().strftime("%y%m%d_%H%M%S")
  dind_env_lines = [
    "# Generated by servers/src/utils/prepare_runtime.py",
    f"RUN_TS={run_ts}",
    f"ENABLED_SERVICES={args.enabled_services}",
    "",
    "# Shared demo credentials used inside the DinD host.",
    f"NEO4J_USER={NEO4J_USER}",
    f"NEO4J_PASSWORD={NEO4J_PASSWORD}",
    f"POSTGRES_USER={POSTGRES_USER}",
    f"POSTGRES_PASSWORD={POSTGRES_PASSWORD}",
    f"POSTGRES_DB={POSTGRES_DB}",
  ]

  # Walk the fixed tunnel assignment order and emit only the derived values
  # needed by the DinD stack.
  for (prefix, role_name), entry in zip(ROLE_ASSIGNMENTS, tunnels):
    if not isinstance(entry, dict):
      raise SystemExit(f"invalid tunnel entry for {prefix}: expected object")

    public_host = normalize_public_host(str(entry.get("url", "")))
    token = str(entry.get("token", "")).strip()
    tunnel_name = str(entry.get("tunnel_name", "")).strip()

    if not public_host or not token:
      raise SystemExit(f"tunnel entry for {prefix} must include non-empty url and token")

    dind_env_lines.append(f"{prefix}_PUBLIC_HOST={public_host}")
    dind_env_lines.append(f"{prefix}_TUNNEL_TOKEN={token}")

  dind_env_path.write_text("\n".join(dind_env_lines) + "\n", encoding="utf-8")

  # Print only derived, non-secret information so operators can verify success
  # without leaking tunnel tokens.
  log_message(log, f"generated {dind_env_path.relative_to(repo_root)}", color="green")
  log_message(log, f"ensured {logs_raw_dir.relative_to(repo_root)} exists", color="green")
  log_message(log, f"enabled services: {args.enabled_services}", color="cyan")
  log_message(log, "tunnel assignments:", color="yellow")
  for (prefix, _), entry in zip(ROLE_ASSIGNMENTS, tunnels):
    public_host = normalize_public_host(str(entry.get("url", "")))
    tunnel_name = str(entry.get("tunnel_name", "")).strip()
    log_message(log, f"{prefix}: {public_host} ({tunnel_name or 'unnamed'})")

  return 0


if __name__ == "__main__":
  raise SystemExit(main())
