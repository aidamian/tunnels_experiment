"""Generate app-owned runtime files from root-provided inputs."""

from __future__ import annotations

import argparse
import sys
from pathlib import Path

SRC_DIR = Path(__file__).resolve().parents[1]
if str(SRC_DIR) not in sys.path:
  sys.path.insert(0, str(SRC_DIR))

from utils.demo_config import (
  APP_BRIDGE_LOCAL_HOST,
  APP_BRIDGE_LOCAL_PORT,
  APP_UI_LOCAL_HOST,
  APP_UI_LOCAL_PORT,
  DEFAULT_APP_ENABLED_SERVICES,
  PGADMIN_DEFAULT_EMAIL,
  PGADMIN_DEFAULT_PASSWORD,
)
from utils.sdk_logging import build_console_logger, log_message


def parse_args() -> argparse.Namespace:
  parser = argparse.ArgumentParser(description="Generate app runtime state for dind-host-app.")
  parser.add_argument("--run-ts", required=True)
  parser.add_argument("--remote-postgres-public-host", required=True)
  parser.add_argument("--app-ui-public-host", required=True)
  parser.add_argument("--app-ui-tunnel-token", required=True)
  parser.add_argument("--postgres-user", required=True)
  parser.add_argument("--postgres-password", required=True)
  parser.add_argument("--postgres-db", required=True)
  parser.add_argument(
    "--enabled-services",
    default=DEFAULT_APP_ENABLED_SERVICES,
    help="Comma-separated app service keys to launch inside dind-host-app.",
  )
  return parser.parse_args()


def main() -> int:
  args = parse_args()
  log = build_console_logger("apps-runtime")
  app_root = Path(__file__).resolve().parents[2]
  repo_root = app_root.parent
  runtime_dir = app_root / ".runtime"
  logs_raw_dir = app_root / "_logs" / "raw"
  dind_env_path = runtime_dir / "dind.env"

  runtime_dir.mkdir(parents=True, exist_ok=True)
  logs_raw_dir.mkdir(parents=True, exist_ok=True)

  lines = [
    "# Generated by apps/src/utils/prepare_runtime.py",
    f"RUN_TS={args.run_ts}",
    f"ENABLED_SERVICES={args.enabled_services}",
    f"REMOTE_POSTGRES_PUBLIC_HOST={args.remote_postgres_public_host}",
    f"APP_UI_PUBLIC_HOST={args.app_ui_public_host}",
    f"APP_UI_TUNNEL_TOKEN={args.app_ui_tunnel_token}",
    f"POSTGRES_USER={args.postgres_user}",
    f"POSTGRES_PASSWORD={args.postgres_password}",
    f"POSTGRES_DB={args.postgres_db}",
    f"APP_BRIDGE_LOCAL_HOST={APP_BRIDGE_LOCAL_HOST}",
    f"APP_BRIDGE_LOCAL_PORT={APP_BRIDGE_LOCAL_PORT}",
    f"APP_UI_LOCAL_HOST={APP_UI_LOCAL_HOST}",
    f"APP_UI_LOCAL_PORT={APP_UI_LOCAL_PORT}",
    f"PGADMIN_DEFAULT_EMAIL={PGADMIN_DEFAULT_EMAIL}",
    f"PGADMIN_DEFAULT_PASSWORD={PGADMIN_DEFAULT_PASSWORD}",
  ]
  dind_env_path.write_text("\n".join(lines) + "\n", encoding="utf-8")

  log_message(log, f"generated {dind_env_path.relative_to(repo_root)}", color="green")
  log_message(log, f"ensured {logs_raw_dir.relative_to(repo_root)} exists", color="green")
  log_message(log, f"enabled services: {args.enabled_services}", color="cyan")
  log_message(log, f"remote PostgreSQL public host: {args.remote_postgres_public_host}", color="cyan")
  log_message(log, f"public app UI host: {args.app_ui_public_host}", color="cyan")
  log_message(log, f"local bridge bind: {APP_BRIDGE_LOCAL_HOST}:{APP_BRIDGE_LOCAL_PORT}", color="yellow")
  log_message(log, f"local app UI bind: {APP_UI_LOCAL_HOST}:{APP_UI_LOCAL_PORT}", color="yellow")
  return 0


if __name__ == "__main__":
  raise SystemExit(main())
